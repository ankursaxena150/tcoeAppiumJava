package DDDD;

/**
 * <p>
 * Execution modes of WebClient.
 * </p>
 */
public enum TDAPI_EXECUTION_MODES {
  /**
   * <p>
   * execution mode is not specified
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  EXECUTION_MODE_NOT_SPECIFIED, // 0
  /**
   * <p>
   * Execution mode is interactive
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  EXECUTION_MODE_INTERACTIVE, // 1
  /**
   * <p>
   * Execution mode is non-interactive
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  EXECUTION_MODE_NON_INTERACTIVE, // 2
}
